<a name="signing-up"><h3>Signing up</h3><hr>
Almost all services of iunv.social require you to log-in. If you already have an account, you may consider <a href="#logging-in">Logging In</a>.<br><br>
Before signing up, you should consider going through the <a href="../privacy">Privacy Policy</a>, <a href="../terms">Terms & Conditions</a> and the <a href="../accountability">Accountability Policy</a>.
<br><br>
When done with them, you should find the <a href="../">home page</a> or go to <a href="../join-us">the sign-up page</a>.<br>
<br>If you are the home page, click on the 'Join-us' button.<br>
<img src="img/join-button.jpg"><br><br>
Now, you will find a screen/dialogue resembling this:<br><img src="img/join-overlook.jpg">
<br><br>
In the first input box, write down your desired username for iunv. You should make it relevant to your persona for making it easier for your friends to find you. 
Next, type in the password that you'll be using for accessing your account. Make it hard for others to guess and easy for you to remember.
Then, write down your email which will be used to authenticate you in circumstances such as you forgetting your password. Also, this will be used to contact you about the details of your account.
<br><br>Click on next. <br><br><br>
Now you will be taken to page like the following:<br>
<img src="img/join-2.5.jpg"><br><br>
Here, fill up the following as directed:
<ul type="square">
	<li><b>Full Name:</b> Write your full name here. For example, Anubhav Singh.</li>
	<li><b>Gender:</b> Choose your appropriate gender.</li>
	<li><b>DOB (Date of Birth):</b> Choose your date of birth in the given sequence: Month - Date - Year.</li>
	<li><b>Country:</b> Choose your appropriate country.</li>
	<li><b>Introduction:</b> Write a short introduction to who you are, your favourite quotes etc.</li>
	<li><b>About Me:</b> Write a short paragraph informing about your persona.</li>
</ul>
<br><br>
This done, you're ready to move to the next page by clicking the Next button.
<br><br>
The next page will look somewhat like this: <br>
<img src="img/join-4.jpg">
<br><br>
Here, you should fill up the given as directed:
<ul type="square">
	<li><b>Profession:</b> Your profession, if any.</li>
	<li><b>Likes:</b> What you like to do, eat, to go to, and whatever you are crazy for!.</li>
	<li><b>Dislikes:</b> Things which seriously piss you off.</li>
	<li><b>Something only you have or can:</b> Something which you have the exclusive right to or are the only one to be able to do! This, is your show off time!</li>
	<li><b>Suggestions for this site:</b> Oh! We just love your suggestions!</li>
</ul>
<br>
Click on Complete and you're all set to enjoy our services!
<hr>
